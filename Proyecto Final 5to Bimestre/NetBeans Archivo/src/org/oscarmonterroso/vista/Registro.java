    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here
        Cliente cliente = new Cliente();
        
        cliente.setNombre(txtNombre.getText());
        cliente.setApellido(txtApellido.getText());
        cliente.setDireccion(txtDireccion.getText());
        cliente.setTelefono(txtTelefono.getText());
        cliente.setEmail(txtEmail.getText());
        
        
        controlador.agregarCliente(cliente);
        controlador.enlistaClientes();
                
        ControlDeUsuarios modsql = new ControlDeUsuarios();
        Usuario mod = new Usuario();

        String pass = new String(txtPassword.getPassword());
        String passCon = new String(txtConfirmarPassword.getPassword());

        if (txtUsuario.getText().equals("") || pass.equals("") || pass.equals("") || txtNombre.getText().equals("") || txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog((null), "Hay campos vacios, plz llenelos");
        } else {

            if (pass.equals(passCon)) {

                String nuevoPass = hash.sha1(pass);

                mod.setUsuarios(txtUsuario.getText());
                mod.setPassword(nuevoPass);
                mod.setNombre(txtNombre.getText());
                mod.setEmail(txtEmail.getText());
                mod.setId_tipo(1);

                txtUsuario.setText("");
                txtPassword.setText("");
                txtConfirmarPassword.setText("");
                txtNombre.setText("");
                txtEmail.setText("");

                if (modsql.registrar(mod)) {
                    JOptionPane.showMessageDialog(null, "Registro Exitoso");
                } else {
                    JOptionPane.showMessageDialog(null, "Error de Registro");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Las contrase√±as no coinciden");
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

